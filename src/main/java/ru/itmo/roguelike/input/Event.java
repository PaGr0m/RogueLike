package ru.itmo.roguelike.input;

/**
 * Types of event which can be performed by player
 */
public enum Event {
    MOVE_UP,                // Движение персонажа вверх
    MOVE_DOWN,              // Движения персонажа вниз
    MOVE_LEFT,              // Движение персонажа налево
    MOVE_RIGHT,             // Движение персонажа направо
}
